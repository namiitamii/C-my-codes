#include<iostream>

using namespace std;


int main(){
	int x , y , a , b , c , d , e;
	cout<<"Enter First number: ";
	cin>>x;
	cout<<"Enter Second number: ";
	cin>>y;
	
	a = x + y;
	b = x - y;
	c = x * y;
	d = x / y;
	e = x % y;
	
	cout<<"x + y = "<<a<<endl;
	cout<<"x - y = "<<b<<endl;
	cout<<"x * y = "<<c<<endl;
	cout<<"x / y = "<<d<<endl;
	cout<<"x & y = "<<e<<endl;
	
		
	return 0;	
}
