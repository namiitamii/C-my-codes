#include<iostream>

using namespace std;


int main(){
	string x = "C";
	string y = "-32768";
	
	cout<<"Enter an character: ";
	cin>>x;
	if (x == "C"){
		cout<<"You entered character: 'C'"<<endl;
		}
	
	cout<<"Enter a signed short value: ";
	cin>>y;
	if (y == "-32768"){
		cout<<"You entered signed short: -32768";
		}
	
	
			
	return 0;	
}
